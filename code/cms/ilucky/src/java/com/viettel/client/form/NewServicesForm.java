public class NewServicesForm {
    String provider;
    String service;
    String sub_service;
    String ws;
    String command;
    String mo_command;

    public NewServicesForm() {
    }

    public String getProvider() {
        return provider;
    }

    public void setProvider(String provider) {
        this.provider = provider;
    }

    public String getService() {
        return service;
    }

    public void setService(String service) {
        this.service = service;
    }

    public String getSub_service() {
        return sub_service;
    }

    public void setSub_service(String sub_service) {
        this.sub_service = sub_service;
    }

    public String getWs() {
        return ws;
    }

    public void setWs(String ws) {
        this.ws = ws;
    }

    public String getCommand() {
        return command;
    }

    public void setCommand(String command) {
        this.command = command;
    }

    public String getMo_command() {
        return mo_command;
    }

    public void setMo_command(String mo_command) {
        this.mo_command = mo_command;
    }
    
    
}
